import '../../../data/models/permit/permit_roles_model.dart';

abstract class PermitRoleStates {
  const PermitRoleStates();
}

class FetchingPermitRoles extends PermitRoleStates {
  const FetchingPermitRoles();
}

class PermitRolesFetched extends PermitRoleStates {
  final PermitRolesModel permitRolesModel;

  const PermitRolesFetched({required this.permitRolesModel});
}

class CouldNotFetchPermitRoles extends PermitRoleStates {
  const CouldNotFetchPermitRoles();
}

class CheckboxSelected extends PermitRoleStates {
  final Datum selectedDatum;

  CheckboxSelected(this.selectedDatum);
}
